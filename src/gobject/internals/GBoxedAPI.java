package gobject.internals;

import gobject.runtime.GType;

import java.util.HashMap;


import com.sun.jna.Library;
import com.sun.jna.Pointer;

/**
 *
 */
public interface GBoxedAPI extends Library {
    static GBoxedAPI gboxed = GNative.loadLibrary("gobject-2.0", GBoxedAPI.class, new HashMap<String, Object>() {
		private static final long serialVersionUID = 1L;
		{
			put(Library.OPTION_TYPE_MAPPER, new GTypeMapper());
		}
	});
    
    GType g_closure_get_type();
    GType g_hash_table_get_type();    
    Pointer g_boxed_copy(GType gtype, Pointer obj);
    void g_boxed_free(GType gtype, Pointer obj);    
}
